// package 05iteration;

public class main {
    public static void main(String[] args) {
        //for loop
        System.out.println("For loop");
        for(int i=1; i<=10; i++){
            System.out.println(i);
        }

        System.out.println("While loop");
        int j = 1;
        while(j<=10){
            System.out.println(j);
            j++;
        }

        System.out.println("Do while loop");
        int k = 1;
        do{
            System.out.println(k);
            k++;
        }while(k<=10);

    }
}
